/**
 * Encode specified string with ROT13 cipher
 * See details:  https://en.wikipedia.org/wiki/ROT13
 *
 * @param {string} str
 * @return {string}
 *
 * @example
 *
 *   'hello' => 'uryyb'
 *   'Why did the chicken cross the road?' => 'Jul qvq gur puvpxra pebff gur ebnq?'
 *   'Gb trg gb gur bgure fvqr!' => 'To get to the other side!'
 *   'ABCDEFGHIJKLMNOPQRSTUVWXYZabcdefghijklmnopqrstuvwxyz'
 *    => 'NOPQRSTUVWXYZABCDEFGHIJKLMnopqrstuvwxyzabcdefghijklm'
 *
 */
function encodeToRot13(str) {
  const position = {
    a: 1,
    b: 2,
    c: 3,
    d: 4,
    e: 5,
    f: 6,
    g: 7,
    h: 8,
    i: 9,
    j: 10,
    k: 11,
    l: 12,
    m: 13,
    n: 14,
    o: 15,
    p: 16,
    q: 17,
    r: 18,
    s: 19,
    t: 20,
    u: 21,
    v: 22,
    w: 23,
    x: 24,
    y: 25,
    z: 26,
    A: 27,
    B: 28,
    C: 29,
    D: 30,
    E: 31,
    F: 32,
    G: 33,
    H: 34,
    I: 35,
    J: 36,
    K: 37,
    L: 38,
    M: 39,
    N: 40,
    O: 41,
    P: 42,
    Q: 43,
    R: 44,
    S: 45,
    T: 46,
    U: 47,
    V: 48,
    W: 49,
    X: 50,
    Y: 51,
    Z: 52,
  };

  function getKeyByValue(object, value) {
    return Object.keys(object).find((key) => object[key] === value);
  }


  const massLeter = str.split('');
  const indexLetter = [];
  let result = '';

  // eslint-disable-next-line no-plusplus
  for (let i = 0; i < massLeter.length; i++) {
    indexLetter[i] = position[massLeter[i]];

    if (indexLetter[i] <= 13) {
      // eslint-disable-next-line operator-assignment
      indexLetter[i] = indexLetter[i] + 13;
    } else if (indexLetter[i] > 13 && indexLetter[i] <= 26) {
      // eslint-disable-next-line operator-assignment
      indexLetter[i] = indexLetter[i] - 13;
    }


    if (indexLetter[i] <= 39 && indexLetter[i] > 26) {
      // eslint-disable-next-line operator-assignment
      indexLetter[i] = indexLetter[i] + 13;
    } else if (indexLetter[i] > 39) {
      // eslint-disable-next-line operator-assignment
      indexLetter[i] = indexLetter[i] - 13;
    }
    // eslint-disable-next-line quotes
    if (massLeter[i] === " ") {
      result += '';
    }
    // eslint-disable-next-line eqeqeq
    if (getKeyByValue(position, indexLetter[i]) == undefined) {
      result += massLeter[i];
    } else {
      result += getKeyByValue(position, indexLetter[i]);
    }
  }
  return result;
}

